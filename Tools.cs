    /// <summary>
    /// Perform the marching cubes algorithm <br/>
    /// Inspired by and data used from: <see href="https://paulbourke.net/geometry/polygonise/"/> <br/>
    /// Note: this version creates disjointed triangles instead of a proper mesh
    /// </summary>
    /// <param name="spheres"></param>
    /// <returns></returns>
    [Obsolete("This version is deprecated as it creates a disjointed mesh, use MarchingCubes instead.")]
    public static Mesh OldMarchingCubes(int3 size, float gridSize, float3 min, Func<float3, float> distanceToSurface) {
        List<(float3, float3, float3)> result = new();

        // calculate the values for all verts
        for (int z = 0; z < size.z; z++)
            for (int y = 0; y < size.y; y++)
                for (int x = 0; x < size.x; x++) {

                    var localMin = min + (new float3(x, y, z) * gridSize);

                    var cubeverts = new float3[8] {
                        localMin,
                        localMin + new float3(0, 0, gridSize),
                        localMin + new float3(gridSize, 0, gridSize),
                        localMin + new float3(gridSize, 0, 0),

                        localMin + new float3(0, gridSize, 0),
                        localMin + new float3(0, gridSize, gridSize),
                        localMin + new float3(gridSize),
                        localMin + new float3(gridSize, gridSize, 0)
                    };

                    var vals = new float[8] {
                        distanceToSurface(cubeverts[0]),
                        distanceToSurface(cubeverts[1]),
                        distanceToSurface(cubeverts[2]),
                        distanceToSurface(cubeverts[3]),

                        distanceToSurface(cubeverts[4]),
                        distanceToSurface(cubeverts[5]),
                        distanceToSurface(cubeverts[6]),
                        distanceToSurface(cubeverts[7]),
                    };

                    int cubeIndex = 0;

                    for (int i = 0; i < 8; i++)
                        if (vals[i] <= 0)
                            cubeIndex |= 1 << i;

                    int edges = _edgeTable[cubeIndex];

                    if (edges == 0) continue; // cube fully inside or outside mesh

                    var verts = new float3[12];

                    // go through each edge pais and their coresponding corners and calculate the positions should they be part of the slices
                    if ((edges & 1) != 0) verts[0] = VertexInterp(cubeverts[0], cubeverts[1], vals[0], vals[1]);

                    if ((edges & 2) != 0) verts[1] = VertexInterp(cubeverts[1], cubeverts[2], vals[1], vals[2]);

                    if ((edges & 4) != 0) verts[2] = VertexInterp(cubeverts[2], cubeverts[3], vals[2], vals[3]);

                    if ((edges & 8) != 0) verts[3] = VertexInterp(cubeverts[3], cubeverts[0], vals[3], vals[0]);

                    if ((edges & 16) != 0) verts[4] = VertexInterp(cubeverts[4], cubeverts[5], vals[4], vals[5]);

                    if ((edges & 32) != 0) verts[5] = VertexInterp(cubeverts[5], cubeverts[6], vals[5], vals[6]);

                    if ((edges & 64) != 0) verts[6] = VertexInterp(cubeverts[6], cubeverts[7], vals[6], vals[7]);

                    if ((edges & 128) != 0) verts[7] = VertexInterp(cubeverts[7], cubeverts[4], vals[7], vals[4]);

                    if ((edges & 256) != 0) verts[8] = VertexInterp(cubeverts[0], cubeverts[4], vals[0], vals[4]);

                    if ((edges & 512) != 0) verts[9] = VertexInterp(cubeverts[1], cubeverts[5], vals[1], vals[5]);

                    if ((edges & 1024) != 0) verts[10] = VertexInterp(cubeverts[2], cubeverts[6], vals[2], vals[6]);

                    if ((edges & 2048) != 0) verts[11] = VertexInterp(cubeverts[3], cubeverts[7], vals[3], vals[7]);

                    // Get the triangle triples from the table found in MarchingCubesTable.cs
                    int[] tris = _triTable[cubeIndex];

                    for (int i = 0; tris[i] != -1 && i < 12; i += 3)
                        result.Add((verts[tris[i + 2]], verts[tris[i + 1]], verts[tris[i]])); // Order reversed due to differing CW/CCW order used by unity
                }

        Mesh mesh = new();

        Vector3[] vertices = new Vector3[result.Count * 3];

        for (int i = 0; i < result.Count; i++) {
            var triangle = result[i];
            vertices[i * 3] = triangle.Item1;
            vertices[i * 3 + 1] = triangle.Item2;
            vertices[i * 3 + 2] = triangle.Item3;
        }

        mesh.SetVertices(vertices);
        mesh.SetTriangles(Enumerable.Range(0, result.Count * 3).ToArray(), 0);

        return mesh;
    }
